# int
data = 1
assert type(data) == int

# float
data = 1.2
assert type(data) == float

# str
data = "orlando"
assert type(data) == str

# bool
data = False
assert type(data) == bool

# list
data = [1, 2, 3]
assert type(data) == list

# tuple
data = (1, 2, 3)
assert type(data) == tuple

# set
data = {1, 2, 3}
assert type(data) == set

# dict
data = {"name": "ren", "age": 25}
assert type(data) == dict
